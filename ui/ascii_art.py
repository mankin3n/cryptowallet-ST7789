# Hexadecimal display decoration
def hex_display(data: str, length: int = 8) -> str:
    """Display data in hex format."""
    hex_str = data.encode().hex()[:length*2]
    return ' '.join([hex_str[i:i+2] for i in range(0, len(hex_str), 2)])

